#include "calculate.h"
int add(int num1, int num2) {
	return num1 + num2;
}

int substract(int num1, int num2) {
	return num1 - num2;
}

int multiply(int num1, int num2) {
	return num1 * num2;
}

int devide(int num1, int num2) {
	return num1 / num2;
}

int exponent(int num1, int num2) {
	return pow(num1, num2);
}